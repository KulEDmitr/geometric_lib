import math


def area(r):
    '''
    This function eval area of circle
        Parameters:
             r: radius of circle
        Return:
            area: area of circle
        Example:
            area(2): 4pi
    '''

    return math.pi * r * r

def perimeter(r):
    '''
    This function eval perimeter of circle
        Parameters:
            r: radius of circle
        Return:
            perimeter: perimeter of circle
        Example:
            perimeter(2): 4pi
    '''

    return 2 * math.pi * r

