import math


def area(r):
    '''
    Takes circle radius value, returns circle area value
    
    Example:
        area(3)
        28.274333882308138
    '''
    return math.pi * r * r


def perimeter(r):
    '''
    Takes circle radius value, returns circuit value
    
    Example:
        perimeter(3)
        18.84955592153876
    '''
    return 2 * math.pi * r

