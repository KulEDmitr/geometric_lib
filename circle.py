import math

'''Подключем библиотеку math'''


def area(r):
    
    '''Функция принимает r <- радиус круга. Возвращает его площадь'''

    return math.pi * r * r



def perimeter(r):
    
    '''Функция принимает r <- радиус круга. Возвращает длину его окружности'''
    
    return 2 * math.pi * r

