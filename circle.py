import math


def area(r):
    return math.pi * r * r
''' 
This function eval area of circle
    Parameters:
          r: radius of circle
    Return:
        area: area of circle
    Example:
        area(2): 2pi
'''

def perimeter(r):
    return 2 * math.pi * r
'''
This function eval perimeter of circle
    Parameters:
        r: radius of circle
    Return:
        perimeter: perimeter of circle
    Example:
        perimeter(2): 4pi
'''
