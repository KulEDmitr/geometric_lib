import math


def area(r):
    '''принимает число r, возвращает произведение числа пи на квадрат числа r.

    Example:
    Input: 2
    Output: 6.283    
    
    '''
    return math.pi * r * r


def perimeter(r):
    '''принимает число r, возвращает произведение числа пи и числа r на 2.

    Example.
    Input: 2
    Output: 12.566

    '''
    return 2 * math.pi * r

