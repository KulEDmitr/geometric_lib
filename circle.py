import math


def area(r):
	'''
	   Принимает радиус окружности(r) и возвращает площадь круга
		stdin:
			1
		stdout:
			3.141592653589793
	'''
	return math.pi * r * r


def perimeter(r):
	'''
	   Принимает радиус окружности(r) и возфращает её длину
		stdin: 
			1
		stdout:
			6.283185307179586
	'''
	return 2 * math.pi * r

