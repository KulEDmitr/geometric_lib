import math


def area(r):
    '''
    Takes the radius of a circle and returns its area
    Example:
    print(area(5)) // 78.53981633974483
    '''
    return math.pi * r * r

def perimeter(r):
    '''
    Takes the radius of a circle and returns its perimeter
    Example:
    print(perimeter(5)) // 31.41592653589793
    '''
    return 2 * math.pi * r
