import math

def area(r):
    '''
    Принимает радиус r и возвращает площадь круга
        Параметры:
            r (int): Радиус круга
            math.pi (floar): Число Pi
    '''
    return math.pi * r * r


def perimeter(r):
    '''
    Принимает радиус r и возвращает периметр круга
    Параметры:
            r (int): Радиус круга
            math.pi (floar): Число π
    '''
    return 2 * math.pi * r

