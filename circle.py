import math


def area(r):
	'''
 This function eval area of circle
        Parameters:
                r: radius of circle
        Return:
                area: area of circle
        Examle of call:
           area(1) : pi
'''
	return math.pi * r * r

def perimeter(r):
	'''
This function eval perimeter of circle
	 Parameters:
 	 	r: radius of circle
	 Returns:
	 	perimeter: perimeter of circle
	 Examle of call:
           perimeter(1) : 2*pi
'''
	return 2 * math.pi * r





