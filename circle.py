import math


def area(r):
    '''
    Parameter: r (int): circle radius
    Return value: area of a circle with radius r
    area(3) #returns 28.26
    '''
    
    return math.pi * r * r


def perimeter(r):
    '''
    Parameter: r (int): circle radius
    Return value: perimeter of a circle with radius r
    perimeter(3) #returns 18.84
    '''

    return 2 * math.pi * r

