import math


def areaCircle(r):
    """
    Принимает число r и возращает вещественное число - площадь круга

    Call example
    Input: 5
    Output: 78.53981633974483
    """
    return math.pi * r * r


def perimeterCircle(r):
    """
    Принимает число r, возращает вещественное число - длину окружности

    Call example
    Input: 5
    Output: 31.41592653589793
    """
    return 2 * math.pi * r

