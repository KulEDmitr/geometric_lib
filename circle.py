import math

def area(r):
    '''Принимает число r, возвращает площадь окружности радиусом r

       Входное значение: 6
       Результат: 113.09733552923255
    '''
    return math.pi * r * r


def perimeter(r):
    '''Принимает число r, возвращает периметр окружности радиусом r

       Входное значение: 6
       Результат: 37.69911184307752
    '''
    return 2 * math.pi * r

