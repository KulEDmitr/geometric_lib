import math


def area(r):
    '''
    Принимает радиус круга r, возвращает значение площади круга
    Input: (4)
    Output: (50.24)
    '''
    return math.pi * r * r


def perimeter(r):
    '''
    Принимает радиус круга r, возвращает значение периметра круга
    Input: (4)
    Output: (25.12)
    '''
    return 2 * math.pi * r

