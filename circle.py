import math


def area(r):
    '''
    Returns area of circle

    Args:
        r: Radius

    Return:
        float: Circle's area
    '''
    return math.pi * r * r


def perimeter(r):
    '''
    Returns perimeter of circle

    Args:
        r: Radius

    Return:
        float: Circle's perimeter
    '''
    return 2 * math.pi * r

