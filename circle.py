import math


def area(r):
    '''
    Возвращает площадь круга радиуса r
    Input: 3
    Output: 28.274333882308138
    '''
    return math.pi * r**2


def perimeter(r):
    '''
    Возвращает периметр круга радиуса r
    Input: 3
    Output: 18.84955592153876 
    '''
    return 2 * math.pi * r

