import math


def AreaCircle(r):
    '''принимает r и возвращает квадрат r умноженный на значение хранящиеся в math.pi'''
    return math.pi * r * r


def PerimeterCircle(r):
    '''принимает r и возвращает r умноженное на значение хранящиеся в math.pi и умноженное на 2'''
    return 2 * math.pi * r

