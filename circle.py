import math

def area(r):
    '''Принимает число r - радиус круга и возвращает площадь круга
    Входные данные
    4
    Выходные данные
    50.24
    '''
    return math.pi * r * r


def perimeter(r):
    '''Принимает число r - радиус круга и возвращает периметр круга(длина окружности)
    Входные данные
    4
    Выходные данные
    25.13274
    '''
    return 2 * math.pi * r

