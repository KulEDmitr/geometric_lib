import math

def area(r):
    '''
    Функция принимает число r и возвращает площадь круга с радиусом r
    
    Пример:
            input: 10    output: 314
    
    '''

    return math.pi * r * r
    


def perimetr(r):
    '''
    Функция принимает число r и возвращает длину окружности с радиусом r

    Пример:
            input: 100      output: 628
    '''
    return 2 * math.pi * r

