import math


def area(r):
    '''
    Takes radius of circle and returns its area
    :print(area(4)) # Output: 50.26548
    '''
    return math.pi * r * r


def perimeter(r):
    '''
    Takes radius of circle and returns its perimeter
    :print(perimeter(4)) # Output: 25.13274
    '''
    return 2 * math.pi * r

