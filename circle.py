import math

def area(r):
    return math.pi * r * r
    '''
    Принимает число r, возвращает площадь окружности с радиусом r.
    
    Напрмер:
        Вход: 6
        Выход: 113.09733552923255
    '''

def perimeter(r):
    return 2 * math.pi * r
    '''
    Принимает число r, возвращает периметр окружности с радиусом r.
    
    Напрмер:
        Вход: 4
        Выход: 25.132741228718345
    '''
