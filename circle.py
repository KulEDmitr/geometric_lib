import math


def area(r):
    '''принимает радиус - возвращает площадь круга
    r = 3
    area = 28.274333882308138
    '''
    return math.pi * r * r


def perimeter(r):
    '''принимает радиус - возвращает периметр круга
    r = 3
    perimeter = 18.84955592153876
    '''
    return 2 * math.pi * r

