import math


def area(r):
    '''
    Возвращает площадь круга
    Принимает число r, возвращает произведение квадрата числа r на рі
    '''
    return math.pi * r * r


def perimeter(r):
    '''
    Возвращает периметр круга
    Принимает число r, позвращает произведение числа на рі и на 2
    '''
    return 2 * math.pi * r

