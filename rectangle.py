def area(a, b):
    ''' Принимает числа a,b, возвращает произведение a и b '''
    '''int(a) = 5, int(b) = 6 -> a * b = 30'''
    return a * b 


def perimeter(a, b):
    ''' Принимает числа a,b, возвращает удвоенную сумму чисел a и b '''
    '''int(a) = 5, int(b) = 6 -> 2 * (a + b) = 22'''
    return 2*a + 2*b 

