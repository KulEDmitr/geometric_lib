def RectangleArea(a, b):
    """
    Принимает числа а, b - стороны прямоугольника;
        Возвращает площадь прямоугольника - произведение чисел a и b

    Call example:
        Input: 10 4
        Output: 40

    """
    return a * b 

def RectanglePerimeter(a, b):
    """
    Принимает числа а, b - стороны прямоугольника;
        Возвращает периметр прямоугльника - удвоенную сумму чисел a и b

    Call example:
        Input: 10 4
        Output: 28

    """
    return (a + b) * 2
