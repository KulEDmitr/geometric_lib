def area(a, b): 
    '''
    Function get a, b sides of rectangle and return a*b
        - area
    Example: area(10, 5) = 10 * 5 = 50
    '''
    return a * b 

def perimeter(a, b):
    '''
    Function get a, b sides and return 2*(a+b)
        - perimeter of rectangle
    Example: perimeter(10, 5) = 2 * (10 + 15) = 50
    '''
    return 2 * (a + b)