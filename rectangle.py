def area(a, b):
    """ Returns area of the rectangle with given sides

    :param a: first side
    :type a: float

    :param b: second side
    :type b: float

    :return: area of the rectangle
    :rtype: float

    area(1.0, 3.5) = 3.5
    """

    return a * b 

def perimeter(a, b):
    """ Returns perimeter of the rectangle with given sides

    :param a: first side
    :type a: float

    :param b: second side
    :type b: float

    :return: perimeter of the rectangle
    :rtype: float

    perimeter(32, 1) = 66
    """

    return (a + b)  * 2

