def area(a, b): 
    """
    Принимает числа a, b. Возвращает площадь прямоугольника со сторонами a и b
    Пример:
		area(52, 2) = 104
    """
    return a * b

def perimeter(a, b):
    """
    Принимает числа a, b. Возвращает периметр прямоугольника со сторонами a и b
    Пример:
		area(52, 2) = 104
    """
    return (a + b) * 2
