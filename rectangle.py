def area(a, b):
    '''
    Принимает длину и ширину прямоугольника и возвращает его площадь
    Пример вызова-
        Входные данные: 9 8
    Выходные данные: 72
    '''
    return a * b 

def perimeter(a, b):
    '''
    Принимает длину и ширину прямоугольника и возвращает его периметр
    Пример вызова-
        Входные данные: 199 356
    Выходные данные: 1110
    '''
    return 2*(a + b) 
