def area(a, b):
    """
    Return the area of the rectangle.

    Args:
        a (int): rectangle length.
        b (int): rectangle width.

    Example:
        area(2, 4) --> 8
    """
    return a * b


def perimeter(a, b):
    """
    Return the perimeter of the rectangle.

    Args:
        a (int): rectangle length.
        b (int): rectangle width.

    Example:
        perimeter(2, 4) --> 12
    """
    return 2 * (a + b)
