def area(a, b):
    '''
    Принимает стороны прямоугольника, возвращает его площадь
    Пример вызова:
        a = 3
        b = 5
        area(a) = 15
    '''
    return a * b

def perimeter(a, b):
    '''
    Принимает стороны треугольника, возвращает его периметр
    Пример вызова:
        a = 3
        b = 5
        area(a) = 16
    '''
    return (a + b) * 2
