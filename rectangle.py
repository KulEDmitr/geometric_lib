def area(a, b):
    '''
    Принимает стороны прямоугольника a и b, возвращает площадь прямоугольника

    Пример входных данных: 3 5
    Результат: 15
    '''
    return a * b


def perimetr(a, b):
    '''
    Принимает стороны прямоугольника a и b, возвращает периметр прямоугольника

    Пример входных данных: 3 5
    Результат: 16
    '''
    return 2 * (a + b)
