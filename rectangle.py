def area(a, b): 
    
    '''Функция принимает значения двух сторон прямоугольника. Возвращает его площадь.'''
    
    return a * b 
    ''' area(4, 3) ; return 12 '''

def perimeter(a, b): 
    
    '''Функция принимает значения двух сторон прямоугольника. Возвращает его периметр.'''
    
    return 2*(a + b)    
    ''' perimeter(4, 3) ; return 14 '''