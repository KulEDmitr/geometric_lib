def area(a, b):
    '''
    Возвращает площадь прямоугольника
    Принимает числа a и b, возвращает их произведение
    '''
    return a * b 

def perimeter(a, b):
    '''
    Возвращает периметр прямоугольника
    Принимает числа a и b, возвращает произведение их суммы на 2
    '''
    return 2*(a + b)
