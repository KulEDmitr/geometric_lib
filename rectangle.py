def area(a, b):
    '''
        Получает на вход a и b - длины сторон прямоугольника. Возвращает значение его площади.
        Пример вызова: area(2, 4) --> 8
    '''
    return a * b 

def perimeter(a, b):
    '''
        Получает на вход a и b - длины сторон прямоугольника. Возвращает значение его периметра.
        Пример вызова: perimeter(2, 4) --> 12
    '''
    return (a + b) * 2 


