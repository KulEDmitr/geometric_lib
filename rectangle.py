def area(a, b): 
    '''принимает числа a и b, возвращает произведение a и b

    Example
    Input: 5 6
    Output: 30
    '''
    return a * b 

def perimeter(a, b): 
    '''принимает числа a и b, возвращает сумму удвоенного значния a с удвоенным значением b
    
    Example
    Input: 5 6   
    Output: 22
    '''
    return a*2 + b*2
