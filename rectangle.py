def area(a, b): 
    '''
    Returns area of ractangle

    Args:
        a: First side
        b: Second side

    Return:
        float: Rectangle's area
    '''
    return float(a * b)

def perimeter(a, b):
    '''
    Returns perimeter of ractangle

    Args:
        a: First side
        b: Second side

    Return:
        float: Rectangle's perimeter
    ''' 
    return float(2 * (a + b))
