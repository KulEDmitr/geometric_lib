def area(a, b):
    '''
    Принимает два числа - a, b - стороны прямоугольника
    Возвращает площадь прямоугольника
    '''
    return a * b 


def perimeter(a, b):
    '''
        Принимает два числа - a, b - стороны прямоугольника
        Возвращает площадь прямоугольника
    '''
    return (a + b) * 2
