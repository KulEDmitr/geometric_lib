def area(a, b):
    '''
    Принимает числа a, b, возвращает площадь прямоугольника со сторонами a и b.
    Пример вызова:
    a = 5
    b = 6
    result = area(a, b)
    print(result)
    Вывод: 30
    '''
    return a * b

def perimeter(a, b):
    '''
    Принимает числа a, b, возвращает периметр прямоугольника со сторонами a и b.
    Пример вызова:
    a = 5
    b = 6
    result = perimeter(a, b)
    print(result)
    Вывод: 22
    '''
    return 2 * (a + b)
