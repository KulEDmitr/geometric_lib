def area(a, b):
	'''
	   Принимает длины сторон прямоугольника(a, b) и возвращает его площадь
		stdin:
			2 4
		stdout: 
			8
	'''
	return a * b
def perimeter(a, b):
	'''
	   Принимает длины сторон прямоугольника(a, b) и возвращает его периметр
		stdin:
			2 4
		stdout:
			12
	'''
	return 2 * (a + b)
