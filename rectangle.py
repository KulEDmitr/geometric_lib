def area(a, b):
    '''
    Принимает числа a и b, возвращает площадь прямоугольника со сторонами a и b
    Code:
    print(area(4, 6))
    Output:
    24
    '''
    return a * b

def perimeter(a, b):
    '''
    Принимает числа a и b, возвращает периметр прямоугольника со сторонами a и b
    Code:
    print(perimeter(4, 6))
    Output:
    20
    '''
    return a * 2 + b * 2
