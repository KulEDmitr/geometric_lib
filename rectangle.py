def area(a, b): 
    '''
    Takes width, height and returns rectangle area:
    :print(perimeter(4, 5)) # Output: 20
    '''
    return a * b 

def perimeter(a, b): 
    '''
    Takes width, height and returns rectangle perimeter
    :print(perimeter(4, 5)) # Output: 18
    '''
    return 2*(a + b)
