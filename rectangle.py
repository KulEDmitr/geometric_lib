def area(a, b): 
    ''' Принимает на вход длины сторон прямоуголька a и b, возвращает площадь прямоугольника
       a = 4, b = 5, return = 20
    '''
    return a * b 
def perimeter(a, b): 
    ''' Принимает на вход длины сторон прямоуголька a и b, возвращает периметр прямоугольника
       a = 4, b = 5, return = 18
    '''
    return (a + b)*2 