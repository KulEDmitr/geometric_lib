def area (a, b):
	'''
	This function eval area of rectangle
		Parameters:
			a: side length of rectangle
			b: side length of rectangle
		Return:
			area: area of rectangle
		Example:
			area(2, 4): 8
	'''

	return a * b

def perimeter (a, b):
	'''
	This function eval perimeter of rectangle
		Parameters:
			a: side length of rectangle
			b: side length of rectangle
		Return:
			perimeter: perimeter of rectangle
		Example:
			perimeter(2, 4): 12
	'''

	return (a + b) * 2

