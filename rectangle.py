def area(a,b):
    '''
    принимает стороны прямоугольника а и b и выводит площадь прямоугольника  - a*b
        пример:
            входные данные --> 4,5
            выходные данные --> 20
    '''
    return a*b
def perimeter(a,b):
    '''
    принимает стороны прямоугольника а и b и выводит периметр прямоугольника - 2*(a+b)
        пример:
            входные данные --> 4,5
            выходные данные --> 18
    '''
    return 2*(a+b)
