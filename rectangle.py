
def area(a, b):
	'''
	Takes rectangle sides length, returns value of rectangle area
	
	Example:
		area(2, 3)
		6
	'''
	return a * b

def perimeter(a, b):
	'''
	Takes rectangle sides length, returns value of rectangle perimeter
	
    Example:
        perimeter(2, 5)
        14
    '''
	return 2 * (a + b)
