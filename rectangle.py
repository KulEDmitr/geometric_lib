# Функция для вычисления площади прямоугольника
def calculate_area(length, width):
    """
    Вычисляет площадь прямоугольника.

    Параметры:
    length (int): Длина прямоугольника.
    width (int): Ширина прямоугольника.

    Возвращает:
    int: Площадь прямоугольника.
    """
    return length * width  # Площадь = длина * ширина


# Функция для вычисления периметра прямоугольника
def calculate_perimeter(length, width):
    """
    Вычисляет периметр прямоугольника.

    Параметры:
    length (int): Длина прямоугольника.
    width (int): Ширина прямоугольника.

    Возвращает:
    int: Периметр прямоугольника.
    """
    return 2 * (length + width)  # Периметр = 2 * (длина + ширина)


# Основная часть программы
if __name__ == "__main__":
    # Запрашиваем у пользователя длину и ширину прямоугольника
    length = int(input("Введите длину прямоугольника: ")) 
    width = int(input("Введите ширину прямоугольника: "))

    # Вычисляем площадь и периметр
    area = calculate_area(length, width)  # Вызываем функцию для вычисления площади
    perimeter = calculate_perimeter(length, width)  # Вызываем функцию для вычисления периметра

    # Выводим результаты
    print(f"Площадь прямоугольника: {area}")  # Выводим площадь
    print(f"Периметр прямоугольника: {perimeter}")  # Выводим периметр
