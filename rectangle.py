def area(a, b):
    """Принимает 2 стороны прямоугольника (int, int)
        возвращает его площадь (int)

        print(area(2, 5)) --> 10"""
    return a * b

def perimeter(a, b):
    """Принимает 2 стороны прямоугольника (int, int)
        возвращает его периметр (int)

        print(perimeter(2, 5)) --> 14"""
    return (a + b) * 2
