def area(a, b): 
    '''Принимает на вход стороны прямоугольника a и b, возвращает площадь прямоугольника
    Пример: area = rectangle.area(4, 3) #Output: 12'''
    return a * b 

def perimeter(a, b): 
    '''Принимает на вход стороны прямоугольника a и b, возвращает периметр прямоугольника
    Пример: per = rectangle.perimeter(4, 3) #Output: 14'''
    return (a + b) * 2
