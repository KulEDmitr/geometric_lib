
def area(a, b): 
    '''
    Принимает число a и b (длина и ширина прямоугольника), возвращает площадь  прямоугольника
    
    Example:
    Input: 5 6
    Output: 30

    '''
    return a * b 

def perimeter(a, b): 
    '''
    Принимает число a и b (длина и ширина прямоугольника), возвращает периметр прямоугольника
    
    Example:
    Input: 5 6
    Output: 22
    
    '''
    return (a + b)*2
    
    