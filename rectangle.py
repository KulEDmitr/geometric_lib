def area(a, b): 
    '''Получает длину сторон a и b, возвращает площадь прямоугольника со сторонами a и b'''
    return a * b 

'''print(area(4, 5)) найдёт площадь прямоугольника со сторонами 4 и 5 (Выведет: 20)'''

def perimeter(a, b): 
    '''Получает длину сторон a и b, возвращает периметр прямоугольника со сторонами a и b'''
    return a*2 + b*2

'''print(perimeter(4)) найдёт периметр прямоугольника со сторонами 4 и 5 (Выведет: 18)'''