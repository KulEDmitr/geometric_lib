def area(a, b): 
    '''Принимает числа a и b - длины сторон прямоугольника, и возвращает площадь прямоугольника
    Входные данные
    4 5
    Выходные данные
    20
    '''
    return a * b 

def perimeter(a, b): 
    '''Принимает числа a и b - длины сторон прямоугольника, и возвращает периметр прямоугольника
    Входные данные
    4 5
    Выходные данные
    18
    '''
    return 2 * (a + b)
