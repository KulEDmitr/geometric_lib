def area(a, b): 
    '''
    Возвращает площадь прямоугольника

    Принимает числа a и b, возвращает произведение a на b.

    Параметры:
        a=2 
        b=3
    Возвращаемое значение:
        6
    '''
    return a * b 

def perimeter(a, b):
    '''
    Возвращает периметр прямоугольника

    Принимает числа a и b, возвращает произведение суммы чисел a и b на 2.
    
    Параметры:
        a=2 
        b=3
    Возвращаемое значение:
        10
    ''' 
    return 2*(a + b)
