def area(a, b):
    '''
    Takes sides of the rectangle and returns its area
    Example:
    Print(area(5,5)) // 25
    '''
    return a * b

def perimeter(a, b):
    '''
    Takes sides of the rectangle and returns its perimeter
    Example:
    Print(area(5,5)) // 20
    '''
    return 2*a + 2*b