def area(a, b):
    """
    Возвращает произведение двух чисел a и b
    """
    return a * b

def perimeter(f, b):
    """
    Возвращает значение периметра прямоугольника со сторонами f и b

    :param f: одна сторона
    :param b: вторая сторона
    :return: 2*(f + b)
    """
    return (f + b) * 2
