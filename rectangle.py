def area(a, b):
    ''' Принимает числа a,b, возвращает произведение a и b
        Пример использования: int(a)=4, int(b)=5; a*b=20
    '''
    return a * b

def perimeter(a, b):
    ''' Принимает числа a,b, возвращает удвоенную сумму чисел a и b
        Пример использования: int(a)=4, int(b)=5; 2*(a+b)=18
    '''
    return 2*(a + b)
