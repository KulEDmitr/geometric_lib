def area(a, b):
    '''
    Возвращает площадь прямоугольника со сторонами a, b

        Параметры:
            a (int): первая сторона прямоугольника
            b (int): вторая сторона прямоугольника

        Возвращаемое значение:
            rectangle_area (int): площадь прямоугольника с заданными стронами a, b
    '''
    return a * b 

def perimeter(a, b):
    '''
    Возвращает периметр прямоугольника со сторонами a, b

        Параметры:
            a (int): первая сторона прямоугольника
            b (int): вторая сторона прямоугольника

        Возвращаемое значение:
            rectangle_perimeter (int): периметр прямоугольника с заданными стронами a, b
    '''
    return 2 * (a + b)
