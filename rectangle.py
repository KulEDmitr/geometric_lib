def area(a, b): 
    # метод принимает два числа: а (длину) и b (ширину). Находит и возвращает их произведение (площадь прямоугольника).
    return a * b 

def perimeter(a, b): 
    # метод принимает два числа: а (длину) и b (ширину). Находит их удвоенную сумму (периметр прямоугольника).
    return (a + b) * 2