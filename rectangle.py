def area(a, b):
    '''принимает числа (a, b) стороны прямоугольника, возвращает (a * b) площадь прямоугольника
    
        #input:  2 3
        #output: 6

        #input:  4 5
        #output: 20'''
    return a * b 

def perimeter(a, b):
    '''принимает числа (a, b) стороны прямоугольника, возвращает 2 * (a + b) периметр прямоугольника
    
        #input:  2 4
        #output: 12

        #input:  3 5
        #output: 16''' 
    return 2 * (a + b) 
