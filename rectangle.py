def area(a, b):
    '''Принимает числа a и b, возвращает площадь прямоугольника со сторонами a и b

       Входные данные: 3 2
       Результат: 6
    '''
    return a * b 

def perimeter(a, b):
    '''Принимает числа a и b, возвращает пириметр прямоугольника со сторонами a и b

       Входные данные: 3 2
       Результат: 12
    '''
    return 2*(a + b)

