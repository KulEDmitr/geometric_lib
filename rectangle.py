def area(a, b):
    ''' Принимает длины сторон прямоугольника, возвращает его площадь '''
    return a * b

"""
Пример работы функции
print(area(10, 5))
50
"""

def perimeter(a, b):
    ''' Принимает длины сторон прямоугольника, возвращает его периметр '''
    return  2 * (a + b)

"""
Пример работы функции
print(perimeter(10, 7))
34
"""