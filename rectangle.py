def area(a, b):
    """ Returns area of the rectangle with given sides

    :param a: first side
    :type a: float

    :param b: second side
    :type b: float

    :return: area of the rectangle
    :rtype: float
    """

    return a * b 

def perimeter(a, b):
    """ Returns perimeter of the rectangle with given sides

    :param a: first side
    :type a: float

    :param b: second side
    :type b: float

    :return: perimeter of the rectangle
    :rtype: float
    """

    return (a + b)  * 2

