def area(a, b):
    '''Принимает числа a и b (стороны прямоугольника), возвращает площадь прямоугольника'''
    return a * b

    '''
    Пример: area(2, 3) == 6
    '''

def perimeter(a, b):
    '''
    Принимает числа a и b (стороны прямоугольника), возвращает периметр прямоугольника
    '''
    return 2 * (a + b)

    '''
    Пример: perimeter(2, 3) == 10
    '''