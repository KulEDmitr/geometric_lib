def area(a, b): 
    return a * b 
    '''
    Принимает числа a и b, возвращает площадь прямоугольника со сторонами a и b.
    
    Напрмер:
        Вход: 8
              3
        Выход: 24
    '''
    
def perimeter(a, b): 
    return (a + b) * 2
    '''
    Принимает числа a и b, возвращает периметр со сторонами a и b.
    
    Напрмер:
        Вход: 4
              7
        Выход: 22
    '''