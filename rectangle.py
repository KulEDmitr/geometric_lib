def area(a, b):
    '''принимает длины двух сторон прямоугольника - возвращает площадь
    a = 3; b = 4
    area = 12
    '''
    return a * b


def perimeter(a, b):
    '''принимает длины двух сторон прямоугольника - возвращает периметр
    a = 3; b = 5
    perimeter = 16
    '''
    return 2 * (a + b)
