def area(a, b): 
    '''
    Принимает стороны a и b прямоугольника и возвращает его площадь
    Пример вызова:
    area(5, 2) -> 10
    '''
    return a * b 

def perimeter(a, b): 
    '''
    Принимает стороны a и b прямоугольника и возвращает его периметр 
    Пример вызова: 
    perimeter(5, 2) -> 14
    '''
    return (a + b) * 2
