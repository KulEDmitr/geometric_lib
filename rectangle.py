def area(a, b):
    """
    Вычисляет площадь прямоугольника.

    Пример использования:
    area(5, 10) # Возвращает 50
    """ 
    return a * b 

def perimeter(a, b):
    """
    Вычисляет периметр прямоугольника.

    Пример использования:
    perimeter(5, 10) # Возвращает 30
    """
    return 2*(a + b)
