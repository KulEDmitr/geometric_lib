
def area(a, b):
    """
    Принимает два числа a и b - стороны прямоугольника, возвращает площадь прямоугольника.
    Примеры вызова функции:
    area(4, 5)
    return: 20
    """
    return a * b


def perimeter(a, b):
    """
    Принимает два числа a и b - стороны прямоугольника, возвращает периметр прямоугольника.
    Примеры вызова функции:
    perimeter(5, 6)
    return: 22
    """
    return 2 * (a + b)
