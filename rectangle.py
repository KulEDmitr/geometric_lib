def area(a, b):
    """
        Принимает числа a, b (ширина и высота), возвращает площадь прямоугольника с такими сторонами
        area(3, 4) -> 12
    """
    return a * b


def perimeter(a, b):
    """
        Принимает число a, b (ширина и высота), возвращает периметр прямоугольника с такими сторонами
        perimeter(3, 4) -> 14
    """
    return 2 * (a + b)
