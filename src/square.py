
def area(a):
    """
    Сalculation area of square

    Args:
        a: Square's side

    Returns:
        float: Square's area

    Example:
        area(1)
        # 1
    """
    return a * a


def perimeter(a):
    """
    Сalculation perimeter of square

    Args:
        a: Square's side

    Returns:
        float: Square's perimeter

    Example:
        perimeter(1)
        # 4
    """
    return 4 * a
