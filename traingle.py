def area(a, h): 
    return a * h / 2 
    '''
    Принимает числа a и h, возвращает площадь треугольника со стороной a и высотой h.
    
    Напрмер:
        Вход: 7
              2
        Выход: 7.0
    '''
 
def perimeter(a, b, c): 
    return a + b + c 
    '''
    Принимает числа a и h, возвращает периметр треугольника со сторонами a, b и c.
    
    Напрмер:
        Вход: 9
              14
              6
        Выход: 29
    '''
