def area(a, b):
    """
    Принимает величину сторон прямоугольника и возвращает его площадь	
    Пример вызова:
    area(2, 3)
    Возвращаемое значение:
    6
    """
    return a * b


def perimeter(a, b):
    """
    Принимает величину сторон прямоугольника и возвращает его периметр	
    Пример вызова:
    perimeter(2, 3)
    Возвращаемое значение:
    10
    """
    return 2 * (a + b) 