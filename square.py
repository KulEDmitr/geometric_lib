
def area(a):
    '''
    Counts the area of the square, using its side
    Parameters:
        a -- length of the side of the square (int or float)
    Return value:
        float -- S of the square
    Example:
        print(area(3)) // prints the square of the circle with side 3
    '''
    return a * a


def perimeter(a):
    '''
    Counts the perimeter of the square, using side
    Parameters:
        a -- length of the side of the square (int or float)
    Return value:
        float -- P of the square
    Example:
        print(perimeter(3)) // prints the perimeter of the square with side 3
    '''
    return 4 * a
