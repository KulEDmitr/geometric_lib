
def area(a):
    '''
    Принимает длину стороны квадрата a, возвращает площадь квадрата
    Input: (4)
    Output: (16)
    '''
    return a * a


def perimeter(a):
    '''
    Принимает длину стороны квадрата a, возвращает периметр квадрата
    Input: (5)
    Output: (20)
    '''
    return 4 * a
