def area(a):
    """
    На вход подаётся a, возвращается a в квадрате.

    При вызове функции area(10)
    10 * 10 = 100
    """
    return a * a


def perimeter(a):
    """
    На вход подаётся a, возвращается a умноженное на 4.

    При вызове функции area(5)
    5 * 4 = 20
    """
    return 4 * a
