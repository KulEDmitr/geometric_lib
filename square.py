
def area(a):
    '''
    Возвращает площадь квадрата
    Принимает число a, возвращает квадрат числа a
    Пример вызова: area(2), функция вернет 4
    '''
    return a * a


def perimeter(a):
    '''
    Возвращает периметр квадрата
    Принимает число a, возвращает произведение числа a на 4
    Пример вызова: perimeter(2), функция вернет 8
    '''
    return 4 * a
