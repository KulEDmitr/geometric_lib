
def area(a):
    """
    Возвращает значение площади квадрата со стороной а
    :param a: сторона квадрата

    Пример запуска:
        area(2) --> 4

    """
    return a * a


def perimeter(a):
    """
    Возвращает значение периметр квадрата со стороной а
    :param a: сторона квадрата

    Пример запуска:
        perimeter(2) --> 8
    """
    return 4 * a
