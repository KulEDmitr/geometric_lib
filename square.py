
def area(a):
    '''
    Принимает число а, возвращает а*а - площадь квадрата со стороной а.
	Examples:
    1.  input : 3
       output : 9
    2.  input : 4
        output : 16
    3.  input : 5
        output : 25
    4.  input : 6
        output : 36
    '''

    return a * a


def perimeter(a):
    '''
    Принимает число a, возвращает a*4 - периметр квадрата со стороной a.
	Examples:
	1.  input : 3
        output : 12
    2.  input : 4
        output : 16
    3.  input : 5
        output : 20
    4.  input : 6
        output : 24
    '''

    return 4 * a
