
def area(a):
    '''
    Принимает сторону квадрата, возвращает площадь квадрата.
        Параметры:
            a: сторона квадрата
        Возращаемое значение:
            Площадь квадрата
    area(5) -> 25
    '''
    return a * a


def perimeter(a):
    '''
    Принимает сторону квадраат, возвращает периметр квадрата.
        Параметры:
            a: сторона квадрата
        Возращаемое значение:
            Периметр квадрата
    perimeter(5) -> 20
    '''
    return 4 * a
