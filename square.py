def area(a):
    '''
    Возвращает площадь крадрата.
        Параметры:
            a (int): сторона квадрата

        Возвращаемое значение:
            area (int): площадь крадрата со стороной a
    '''
    return a * a


def perimeter(a):
    '''
    Возвращает периметр крадрата.
        Параметры:
            a (int): сторона квадрата

        Возвращаемое значение:
            perimeter (int): периметр крадрата со стороной a
    '''
    return 4 * a
