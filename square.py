def area(a):
    """
    Принимает длину стороны квадрата a и возвращает его площадь.

    Пример:
        Ввод:
        a = 5

        Вывод:
        25
    :param a: Длина стороны a.
    :return: Площадь.
    """
    return a * a


def perimeter(a):
    """
    Принимает длину стороны квадрата a и возвращает его периметр.

    Пример:
        Ввод:
        a = 5

        Вывод:
        20
    :param a: Длина стороны a.
    :return: Периметр.
    """
    return 4 * a
