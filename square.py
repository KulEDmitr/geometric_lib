def area(a):
    """
    Return the area of the square.

    Args:
        a (int): square side length.

    Example:
        area(3) --> 9
    """
    return a * a


def perimeter(a):
    """
    Return the perimeter of the square.

    Args:
        a (int): square side length.

    Example:
        perimeter(3) --> 12
    """
    return 4 * a
