
def area(a):
    '''
        Принимает площадь квадрата
        Возвращает площадь квадрата
        area(a): площадь квадрата со стороной а
    '''
    return a * a


def perimeter(a):
    '''
        Принимает сторону квадрата
        Возвращает периметр квадрата
        perimeter(a): периметр квадрата со стороной a
    '''
    return 4 * a
