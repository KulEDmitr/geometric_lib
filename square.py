
def SquareArea(a):
    """
    Принимает число a - сторону квадрата;
        Возвращает площадь квадрата - квадрат числа a

    Call example:
        Input: 10
        Output: 100

    """
    return a * a


def SquarePerimeter(a):
    """
    Принимает число a - сторону квадрата;
        Возвращает периметр квадрата - произведение числа а и числа 4

    Call example:
        Input: 10
        Output: 40

    """
    return 4 * a
