
def area(a):
    '''
Returns the area of square with given side

Parameters: 
    int a: side of the square

Return value:
    (int): area of the square with given side
'''
    return a * a


def perimeter(a):
    '''
Returns the perimeter of square with given side

Parameters: 
    int a: side of the square

Return value:
    (int): perimeter of square with given side
'''
    return 4 * a
