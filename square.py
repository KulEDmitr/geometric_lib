
def area(a):
    """
    Принимает число a, возвращает квадрат числа n (площадь квадрата)
    
    Параметры:
        a (int) : сторона квадрата
        
    Возвращаемое значение:
        a^2 (int) 
    """
    return a * a


def perimeter(a):
    """ Принимает число а, возвращает 4 * а (периметр квадрата) """
    return 4 * a
