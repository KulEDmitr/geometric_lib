
def area(a):
    ''' 
	Returns the area of a square by the given side length
	
		Parameters:
			a (int) : the side of a given square

		Returned value:
			a * a (int) : area of a square with given side length

	Usage:
  		area(5) = 5 * 5 = 25
	'''
    return a * a


def perimeter(a):
    ''' 
	Returns the area of a square with given side length
	
		Parameters:
			a (int) : side of a given square

		Returned value:
			4 * a (int) : perimeter of a square with given side length
	
	Usage:
  		perimeter(5) = 4 * 5 = 20
	'''
    return 4 * a
