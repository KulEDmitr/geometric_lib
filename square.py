
def area(a):
    ''' 
    принимает сторону квадрата a и выводит его площадь - а^2
        пример:
            входные данные --> 4
            выходные данные --> 16
    '''
    return a * a
    

def perimeter(a):
    '''
    принимает сторону квадрата a и выводит его периметр - а*4
        пример:
            входные данные --> 4
            выходные данные --> 16
    '''
    return 4 * a
