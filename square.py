
def area(a):
    """ Returns area of the square with given side

    :param a: side of th square
    :type a: float

    :return: area of the square
    :rtype: float
    """

    return a * a


def perimeter(a):
    """ Returns perimeter of the square with given side

    :param a: side of th square
    :type a: float

    :return: perimeter of the square
    :rtype: float
    """

    return 4 * a
