def area(a):
    
    '''Функция принимает значение стороны квадрата. Возвращает его площадь'''
    
    return a * a
    ''' area(5) ; return 25 '''

def perimeter(a):
    
    '''Функция принимает значение стороны квадрата. Возвращает его периметр'''
    
    return 4 * a
    ''' perimeter(5) ; return 20 '''