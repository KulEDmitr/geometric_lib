
def area(a):
    '''
    Function get a side and return a^2
        - area of square
    Example: area(10) = 10 * 10 = 100
    '''
    return a * a


def perimeter(a):
    '''
    Functin get a side and return 4*a
        - perimeter of square
    Example: perimeter(10) = 4 * 10 = 40
    '''
    return 4 * a
