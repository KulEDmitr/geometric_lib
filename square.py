
def area(a):
    '''
    считает площадь квадрата
    принимает число a - сторона квадрата
    возвращает a^2 - площадь квадрата
    например area(2) = 4
    '''
    return a * a


def perimeter(a):
    '''
    считает периметр квадрата
    принимает число a - сторона квадрата
    возвращает 4*a - периметр квадрата
    например area(2) = 8
    '''
    return 4 * a
