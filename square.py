def area(a):
    '''Получает длину сторон a, возвращает площадь квадрата со сторонами a'''
    return a * a

'''print(area(4)) найдёт площадь квадрата со стороной 4 (Выведет: 16)'''

def perimeter(a):
    '''Получает длину стороны a, возвращает периметр квадрата со сторонами a'''
    return 4 * a

'''print(perimeter(4)) найдёт периметр квадрата со стороной 4 (Выведет: 16)'''