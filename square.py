def area(a):
    """
    Принимает число a (сторона квадрата), возвращает площадь квадрата.

        Пример вызова функции:

            a = 5 # Length of the side
            S = area(a)
            print("Area is equal to", S) # Output: "Area is equal to 25".
    """
    return a * a


def perimeter(a):
    """
    Принимает число a (сторона квадрата), возвращает периметр квадрата.

        Пример вызова функции:

            a = 5 # Length of the side
            P = perimeter(a)
            print("Perimeter is equal to", P) # Output: "Perimeter is equal to 20".
        """
    return 4 * a
