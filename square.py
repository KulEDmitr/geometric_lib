def area(a):
    '''
    Функция считает и возвращает площадь квадрата.
    Аргументы:
        a (float) или (int) - длина стороны квадрата.
    Возвращаемое значение:
        S (float) или (int) - значение площади квадрата,
            (float), если a (float),
            (int), если a (int).
    '''
    return a * a


