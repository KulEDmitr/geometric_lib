def area(a):
    '''Принимает число a (сторона квадрата), возвращает площадь квадрата'''
    return a * a

    '''
    Пример: area(10) == 100
    '''

def perimeter(a):
    '''Принимает число a (сторона квадрата), возвращает периметр квадрата'''
    return 4 * a

    '''
    Пример: perimeter(10) == 40
    '''
