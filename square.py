
def area(a):
    '''Принимает число a(сторона квадрата), возвращает площадь квадрата с данным a'''
    return a * a
    '''
    Пример: 

        Входные данные:5

        Вывод:25
    '''


def perimeter(a):
    '''Принимает число a(сторона квадрата), возвращает периметр квадрата с данным a'''
    return 4 * a
    '''
    Пример: 

        Входные данные:5

        Вывод:20
    '''
