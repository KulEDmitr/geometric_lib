def area(a):
    '''
    принимает на вход сторону квадрата a, возвращает площадь квадрата со стороной a
    Ex: >> area(3)
        >> 9
    '''
    return a * a


def perimeter(a):
    '''
    принимает на вход сторону квадрата a, возвращает периметр квадрата со стороной a
    Ex: >> perimeter(3)
        >> 12
    '''
    return 4 * a
