
def area(a):
    """
    Принимает число a - сторону квадрата, возвращает площадь квадрата.
    Пример вызова функции:
    area(6)
    return: 36
    """
    return a * a


def perimeter(a):
    """
    Принимает число a - сторону квадрата, возвращает периметр квадрата.
    Пример вызова функции:
    perimeter(5)
    return: 20
    """
    return 4 * a
