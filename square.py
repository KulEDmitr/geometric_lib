def area(a):
    '''
    Принимает число a, возвращает площадь квадрата со стороной a
    Code:
    print(area(5))
    Output:
    25
    '''
    return a * a


def perimeter(a):
    '''
    Принимает число a, возвращает периметр квадрата со стороной a
    Code:
    print(perimeter(5))
    Output:
    20
    '''
    return 4 * a
