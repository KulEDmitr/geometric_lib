def area(a):
    '''
    Принимает число a, возвращает площадь квадрата со стороной a.
    Пример вызова:
    a = 5
    result = area(a)
    print(result)
    Вывод: 25
    '''
    return a * a

def perimeter(a):
    '''
    Принимает число a, возвращает периметр квадрата со стороной a.
    Пример вызова:
    a = 5
    result = perimeter(a)
    print(result)
    Вывод: 20
    '''
    return 4 * a
