def area(a):
    '''
    Принимает сторону квадрата, возвращает его площадь
    Пример вызова:
        a = 3
        area(a) = 9
    '''
    return a * a


def perimeter(a):
    '''
    Принимает сторону квадрата, возвращает его периметр
    Пример вызова:
        a = 3
        area(a) = 12
    '''
    return 4 * a
