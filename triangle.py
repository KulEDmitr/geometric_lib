def area(a, h): 
    '''принимает числа a(длина основания треугольника) и h(высота треугольника), возвращает площадь треугольника
    
    area(5, 6)

    >>> 15.0
    '''
    return a * h / 2 

def perimeter(a, b, c): 
    '''принимает числа a, b, c(длины сторон треугольника), возвращает периметр тругольника
    
    perimeter(3, 4, 5)

    >>> 12
    '''
    return a + b + c 
