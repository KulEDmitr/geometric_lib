
def area(a, h):
    '''
    Takes lengths of the triangle side and triangle height, returns area of the triangle

    Example:
        area(3, 4)
        6
    '''
    return a * h / 2

def perimeter(a, b, c):
    '''
    Takes lengths of the triangle sides, returns perimeter of the triangle

    Example:
        perimeter(3, 2, 5)
        10
    '''
    return a + b + c
