def area(a, h): 
    '''Принимает длину стороны треугольника и проведенной к ней высоты, возвращает площадь треугольника;
       Ex: area(3, 5) -> 7,5 ''' 
    return a * h / 2 

def perimeter(a, b, c): 
    '''Принимает длины сторон треугольника, возвращает его периметр;
       Ex: perimeter(3, 6, 6) -> 15 '''
    return a + b + c 