
def area(a, h): 
    '''принимает на вход числа a и h, возвращает произведение этих чисел, деленное на 2
    
    Example
    Input: 4 5
    Output: 10
    '''
    return a * h / 2 

def perimeter(a, b, c): 
    '''принимает на вход числа a, b, c, возвращает сумму этих чисел

    Example
    Input: 4 5 6
    Output: 15
    '''
    return a + b + c 
