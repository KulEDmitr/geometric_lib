def area(a, h): 
    '''
    Function get a side and h tall and return (a*h)/2 
        - area of trinangle
    Example: area(5, 2) = 5 * 2 / 2 = 5
    '''
    return a * h / 2 

def perimeter(a, b, c):
    '''
    Function get a, b, c sides of triangle and return a+b+c
        - perimeter
    Example: perimeter(2, 4, 3) = 2 + 4 + 3 = 9
    '''
    return a + b + c