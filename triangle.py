def area(a, h):
    """Принимает числа a, h (длина стороны и высоты, опущенной к этой стороне),
     возвращает площадь треугольника с такой стороной и опущенной к ней высотой"""
    return a * h / 2


def perimeter(a, b, c):
    """Принимает числа a, b, c (длина сторон треугольника), возвращает треугольника с такими сторонами"""
    return a + b + c
