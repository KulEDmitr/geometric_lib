def area(a, h):
    #Принимает a - сторона треугольника к которой проведена высота h, и
    #Принимает h - высота треугольника, возвращает площадь треугольника
    return a * h / 2 

def perimeter(a, b, c):
    #Принимает a, b, c  - стороны треугольника, возвращает периметр треугольника
    return a + b + c