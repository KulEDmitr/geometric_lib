def area(a, h):
    """
    Принимает два числа a - сторона и h - высота, возвращает площадь треугольника.
    Пример вызова функции:
    area(6, 8)
    return: 24.0
    """
    return a * h / 2

def perimeter(a, b, c):
    """
    Принимает три числа a, b, c - стороны треугольника, возвращает их периметр.
    Пример вызова функции:
    perimeter(3, 4, 5)
    return: 12
    """
    return a + b + c