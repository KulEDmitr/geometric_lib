def area(a, h): 
    '''
    Принимает сторону a и высоту h треугольника и возвращает его площадь 
    Пример вызова: 
    area(10, 6) -> 30
    '''
    return a * h / 2 

def perimeter(a, b, c): 
    '''
    Принимает стороны a, b, c треугольника и возвращает его периметр 
    Пример вызова: 
    perimeter(10, 5, 6) -> 21
    '''
    return a + b + c 
