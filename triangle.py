def area(a, h):
    ''' Принимает числа a и h, возвращает половину произведения a и h '''
    '''int(a) = 5, int(h) = 8 -> a * h * 0.5 = 20'''
    return a * h / 2 


def perimeter(a, b, c):
    ''' Принимает числа a,b,c, возвращает сумму a,b,c '''
    '''int(a) = 5, int(b) = 6, int(c) = 3 -> a + b + c = 14'''
    return a + b + c 

