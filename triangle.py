def area(a, h):
    '''
    Возвращает площадь треугольника
    Принимает числа а и h, возвращает частное от деления их произведения на 2
    '''
    return a * h / 2 

def perimeter(a, b, c):
    '''
    Возвращает периметр треугольника
    Принимает числа а, b и с, возвращает их сумму
    '''
    return a + b + c
