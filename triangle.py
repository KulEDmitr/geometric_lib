def area(a, h):
    '''
    Принимает длину стороны и высоту треугольника, возвращает его площадь.
    
    Пример вывода:
      10 15
      75
    '''
    return a * h / 2 

def perimeter(a, b, c):
    '''
    Принимает длины сторон треугольника, возвращает его периметр.

    Пример вывода:
      10 15 20
      45
    '''
    return a + b + c 
