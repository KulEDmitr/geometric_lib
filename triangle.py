def area(a, h): 
    return a * h / 2 
    '''
    считает площадь треугольника
    принимает числа a,h: a - сторона треугольника, h - высота, проведённая к a
    возвращает a*h/2 - площадь треугольника 
    например area(2,3) = 3
    '''
def perimeter(a, b, c): 
    return a + b + c 
    '''
    считает периметр треугольника
    принимает числа a,b,c - стороны треугольника
    возвращает a+b+c - периметр треугольника 
    например area(2,3,4) = 9
    '''
