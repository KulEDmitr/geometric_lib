def area(a, h):
    '''Принимает числа a,h - длина стороны треугольника и его высота, и возвращает площадь треугольника
    Входные данные
    5 4
    Выходные данные
    10
    '''
    return a * h / 2 

def perimeter(a, b, c): 
    '''Принимает числа a,b,c - длины стороны треугольника, и возвращает периметр треугольника
    Входные данные
    5 5 5
    Выходные данные
    15
    '''
    return a + b + c 
