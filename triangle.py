def area(a, h):
    """ Returns area of the triangle with given side and height drawn to this side

    :param a: side
    :type a: float

    :param h: height
    :type h: float

    :return: area of the triangle
    :rtype: float
    """

    return a * h / 2 

def perimeter(a, b, c):
    """Returns area of the triangle with three given sides

    :param a: first side
    :type a: float

    :param b: second side
    :type b: float

    :param c: third side
    :type c: float

    :return: perimeter of the triangle
    :rtype: float
    """

    return a + b + c 