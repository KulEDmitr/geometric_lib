def area(a, h): 
    '''
    Возвращает площадь треугольника.

            Параметры:
                    a (int/float): длина стороны треугольника
                    h (int/float): длина высоты треугольника
            
            Возвращаемое значение: площадь треугольника (float)
    '''
    return a * h / 2 

def perimeter(a, b, c): 
    '''Принимает длины сторон a, b, c треугольника, возвращает периметр треугольника'''
    return a + b + c 
