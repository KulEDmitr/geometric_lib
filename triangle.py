def area(a, h):
    '''
    Принимает основания и высоты треугольника, возвращает его площадь
    Пример вызова;
        Входные данные: 5 10
    Выходные данные: 25
    '''
    return a * h / 2 

def perimeter(a, b, c):
    '''
    Принимает длины всех сторон треугольника и возвращает его периметр
    Пример вызова:
    Входные данные: 100 100 100
    Выходные данные: 300
    '''
    return a + b + c 
