def area(a, h): 
    '''
        Возращает площадь треугольника.

        Параметры:
            a (int) - сторона треугольника.
            h (int) - высота треугольника.

        Возращаемое значение:
            s (int) - площадь треугольника.
    '''
    s = a * h / 2
    return s

def perimeter(a, b, c):
    '''
        Возращает периметр треугольника.

        Параметры:
            a (int) - сторона треугольника.
            b (int) - сторона треугольника.
            c (int) - сторона треугольника.

        Возращаемое значение:
            p (int) - площадь треугольника.
    '''
    p = a + b + c
    return p