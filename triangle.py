def area(a,h):
    '''
    Возвращает площадь треугольника со стороной a и проведенной к ней высоте h
    Input: 3, 2
    Output: 3
    '''
    return a*h/2

def perimeter(a,b,c):
    '''
    Возвращает периметр треугольника со сторонами а, b, c
    Input: 3, 4, 5
    Output: 12
    '''
    return a+b+c

