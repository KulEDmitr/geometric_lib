def area(a, h): 
    '''  Принимает длину стороны тругольника и длины высоты треугольника, проведенной к ней.
        Возвращает площадь треугольника
        a = 5, h = 4, return = 10
    '''
    return a * h / 2 

def perimeter(a, b, c): 
    ''' Принимает длины трех сторон треугольника и ворзвращает его периметр
        a = 5, b = 4, c = 3, return = 12
    '''
    return a + b + c
