def area(a, h):
    '''
        Параметры: a - сторона треугольника, h - высота треугольника, проведенная к этой стороне или ее продолжению
        Возвращает площадь треугольника
        area(a, h): площадь такого треугольника
    '''
    return a * h / 2

def perimeter(a, b, c):
    '''
        Параметры: a, b, c - стороны треугольника
        Возвращает периметр треугольника
        perimetr(a, b, c): периметр такого треугольника
    '''
    return a + b + c
