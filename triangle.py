def area(a, h):
    ''' Принимает длину основания и высоту треугольника, возвращает его площадь'''
    return a * h / 2

def perimeter(a, b, c):
    ''' Принимает длины трёх сторон треугольника, возвращает его периметр'''
    return a + b + c

