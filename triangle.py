def area(a, h): 
    
    '''
    Принимает число a, h (основание и высота треугольника) возвращает площадь треугольника
    
    Example:
    Input: 1 2
    Output: 1
    
    '''
    return a * h / 2 
def perimeter(a, b, c): 
    '''
    Принимает число a, b, c (стороны треугольника), возвращает периметр треугольника
    
    Example:
    Input: 1 2 3
    Output: 6
    
    '''
    
    return a + b + c
