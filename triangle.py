def area(a, h):
    '''Принимает числа a и h, возвращает площадь треугольника со стороной a и высотой h

       Входные данные: 3 2
       Результат: 3
    '''
    return a * h / 2 

def perimeter(a, b, c):
    '''Принимает числа a, b и c, возвращает перимитр треугольника со сторонами a, b и c

       Входные данные: 3 2 3
       Результат: 8
    '''
    return a + b + c 

