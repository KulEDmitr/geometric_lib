def area(a, h):
    '''
	принимает сторону треугольника a и высоту проведенную к ней h и считает его площадь - a*h/2
		пример:
        	входные данные --> 4, 2
        	выходные данные --> 4
	'''
    return a * h / 2


def perimeter(a, b, c):
    '''
	принимает стороны треугольника a b c и выводит его периметр - a+b+c
		пример:
        	входные данные --> 4, 3 ,2
        	выходные данные --> 9
 	'''
    return a + b + c
