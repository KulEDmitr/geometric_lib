def area(a, h):
	'''
	   Принимает длину сонования(a) и высоту(h), возвращает площадь треугольника
		stdin:
			2 5
		stdout:
			5
	'''
	return a * h / 2
def perimeter(a, b, c):
	'''
	   Принимает длины стороны треугольника(a, b, c) и возвращает его периметр
		stdin:
			1 2 3
		stdout:
			6
	'''
	return a + b + c
