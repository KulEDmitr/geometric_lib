def area(a, h):
    """
    Вычисляет площадь треугольника.

    Пример использования:
    area(5, 10) # Возвращает 25
    """ 
    return a * h / 2 

def perimeter(a, b, c):
    """
    Вычисляет периметр треугольника.

    Пример использования:
    area(3, 4, 5) # Возвращает 12
    """
    return a + b + c