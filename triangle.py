def area(a, h):
    '''принимает числа (a, h) снование и высоту треугольника, возвращает (a * h / 2) площадь треугольника
        #input:  2 3
        #output: 3.0

        #input:  5 3
        #output: 7.5'''
    return a * h / 2 

def perimeter(a, b, c):
    '''принимает числа (a, b, c) стороны треугольника, возвращает (a + b + c) сумму всех сторон
    
        ##input:  3 4 5
        #output: 12

        #input:  5 5 6
        #output: 16'''
    return a + b + c 
