def area(a, h):
    '''
    Принимает сторону (на которую падает высота) и высоту треугольника, возвращает его площадь
    Пример вызова:
        a = 2
        h = 5
        area(a) = 10
    '''
    return a * h / 2

def perimeter(a, b, c):
    '''
    Принимает стороны треугольника, возвращает его периметр
    Пример вызова:
        a = 4
        b = 3
        c = 5
        area(a) = 12
    '''
    return a + b + c 
