def area(a, h):
    '''Принимает числа a и h - сторона и проведенная к ней высота треугольника. Возвращает площадь данного треугольника.'''
    return a * h / 2 

def perimeter(a, b, c): 
    '''Принимает числа a и h - сторона и проведенная к ней высота треугольника. Возвращает периметр данного треугольника.'''
    return a + b + c
