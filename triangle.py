def area(a, h):
    '''
    Принимает числа a и h, возвращает площадь треугольника с
      основанием a и высотой h
    Code:
    print(area(4, 7))
    Output:
    14.0
    '''
    return a * h / 2 

def perimeter(a, b, c):
    '''
    Принимает числа a, b и c, возвращает периметр треугольника со
      сторонами a, b и c
    Code:
    print(perimeter(2, 3, 4))
    Output:
    9
    '''
    return a + b + c 
