def area(a, h): 
    
    '''Функция принимает значения высоты треугольника и сторону, к которой проведена высота. Возвращает его площадь'''
    
    return a * h / 2 


def perimeter(a, b, c): 
    
    '''Функция принимает значения трех сторон треугольника. Возвращает его периметр.'''
    
    return a + b + c 