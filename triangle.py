def area(a, h):
    '''
    Возвращает площадь треугольника
    Принимает числа a и h, возвращает частное от деления их произведения на 2
    Пример вызова: area(2, 3), функция вернет 3
    '''
    return a * h / 2 

def perimeter(a, b, c):
    '''
    Возвращает периметр треугольника
    Принимает числа a, b и c, возвращает сумму
    Пример вызова: perimeter(2, 3, 4), функция вернет 9
    '''
    return a + b + c
