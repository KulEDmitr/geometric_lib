def area(a, h):
    ''' Принимает числа a и h, возвращает полупроизведение a и h '''
    ''' Пример использования: int(a)=10, int(h)=12; a*h*0.5=60'''
    return a * h / 2

def perimeter(a, b, c):
    ''' Принимает числа a, b, c, возвращает сумму a, b, c'''
    ''' Пример использования: int(a)=3, int(b)=4, int(c)=5; a+b+c=12'''
    return a + b + c
