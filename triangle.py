def area(a, h):
    """
    Принмает a, h, возвращает площадь треугольника с стороной a, и высотой опущенной к этой стороне h
    Пример
    input 10 4
    output 20
    """
    return a * h / 2

def perimeter(a, b, c):
    """
    Принимает a, b, c, возвращает периетр треугольника с сторонами a, b, c
    Пример
    input: 3 4 5
    output: 12
    """    
return a + b + c

