def area(a, h):
    '''
    Takes base and height the triangle and returns its area
    Example:
    Print(perimeter(10,5)) // 25
    '''
    return a * h / 2

def perimeter(a, b, c):
    '''
    Takes sides the triangle and returns its perimeter
    Example:
    Print(perimeter(5,5,5)) // 15
    '''
    return a + b + c 