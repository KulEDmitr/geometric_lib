def area(a, h):
    '''Принимает значение a, h, возвращает площадь треугольника со стороной а и проведенной к ней высотой h. Example: a = 3, b = 4 => S = 6'''
    return a * h / 2

def perimeter(a, b, c):
    '''Принимает значения a, b, c, возвращает их сумму. Example: a = 4, b = 2, c = 3 => P = 9'''
    return a + b + c 
