def area(a, h): 
    """
    Принимает числа a, h. Возвращает площадь треугольника со стороной a и высотой h
    Пример:
		area(52, 2) = 52
    """
    return a * h / 2

def perimeter(a, b, c): 
    """
    Принимает числа a, b, c. Возвращает периметр треугольника со сторонами a, b и c
    Пример:
		perimeter(52, 52, 52) = 156
    """
    return a + b + c