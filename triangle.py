def area(a, h):
    '''Принимает число a и h (основание и высота треугольника), возвращает площадь треугольника'''
    return a * h / 2

    '''
    Пример: area(10, 5) == 25
    '''

def perimeter(a, b, c):
    '''Принимает три числа a, b, c (стороны треугольника), возвращает периметр треугольника'''
    return a + b + c

    '''
    Пример: perimeter(3, 4, 5) == 12
    '''