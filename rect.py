def area(a, b):
	return a * b
	'''Приниает a и b,возвращает площадь прямоугольника со сторонами a и b
		Например:
        	area(5, 9)
        	45
	'''

def perimeter(a, b):
	return 2 * (a + b)	
	'''Принимает a и b, возвращает периметр прямоугольника со стронами a и b
		Например:
        	perimeter(5, 9)
        	28
	'''